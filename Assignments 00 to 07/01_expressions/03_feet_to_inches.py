# PROBLEM STATEMENT :

# Converts feet to inches. Feet is an American unit of measurement. There are 12 inches per foot. Foot is
#  the singular, and feet is the plural.


# SOLUTION :

def main():
    """📏 Program to Convert Feet into Inches 🌟"""
    
    # Prompt the user to enter the value in feet 🌟
    feet: float = float(input("\n🦶 Enter the value in feet: ➡️ "))
    
    # Convert the feet into inches (1 foot = 12 inches) 🔄
    inches = feet * 12

    # Display the result with a fun twist 🎉
    print(f"\n ✨ {feet} feet = {inches} inches 📐")

if __name__ == "__main__":
    main()

